package chap_01;

public class _07_TypeCasting {
    public static void main(String[] args) {

        // int to float, double
        int score = 90;
        System.out.println(score);
        System.out.println((float) score);
        System.out.println((double) score);

        // float, double to int
        float score_f = 93.3F;
        double score_d = 98.8;
    }
}
